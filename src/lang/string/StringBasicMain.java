package lang.string;

public class StringBasicMain {
    public static void main(String[] args) {
        //int, boolean,char -> 소문자로 시작 하여 primitive type
        String str = "hello";
        String str2 = new String("hello");
        System.out.println("str2 = " + str2);
        System.out.println("str = " + str);
    }
}
